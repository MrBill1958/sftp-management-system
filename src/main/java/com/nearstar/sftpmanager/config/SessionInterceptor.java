import org.springframework.web.servlet.HandlerInterceptor;

@Slf4j
@Component
public class SessionInterceptor implements HandlerInterceptor
{

    @Override
    public boolean preHandle( HttpServletRequest request, HttpServletResponse response, Object handler ) throws Exception
    {
        String uri = request.getRequestURI();

        // Skip session validation for login/logout endpoints
        if ( uri.contains( "/auth/login" ) || uri.contains( "/auth/logout" ) ||
                uri.contains( "/static" ) || uri.contains( "/css" ) || uri.contains( "/js" ) )
        {
            return true;
        }

        // Add your session validation logic here
        log.debug( "Processing request: {}", uri );

        // For now, allow all requests through
        return true; // ADD THIS MISSING RETURN STATEMENT
    }
}
